    private Boolean nombreEstaCorrecto() {
        Pattern p = Pattern.compile("(^[A-Za-z0-9\s?ñ]{1,25}$)");
        Matcher m = p.matcher(txtNombre.getText());
        return m.matches();
    }

    private void limpiar() {
        txtNombre.setText("");
        txtContra.setText("");
        txtCapacidad.setText("");
        jLabel2.setForeground(new Color(51, 51, 51));
        jLabel3.setForeground(new Color(51, 51, 51));
        jLabel4.setForeground(new Color(51, 51, 51));
        cargarTabla();
    }

    private Boolean allCorrect() {
        if (!nombreEstaCorrecto()) {
            JOptionPane.showInternalMessageDialog(null, "Solo se admiten hasta 25 caracteres alfanumericos", "Nombre Incorrecto", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (!contrasenaEstaCorrecta()) {
            JOptionPane.showMessageDialog(null, "Solo se permiten caracteres Alfanumericos entre 5 y 9", "Contrasena Incorrecta", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (!capacidadEstaCorecta()) {
            JOptionPane.showMessageDialog(null, "Unicamente se permiten numeros enteros entre 1 y 9", "Capacidad Incorrecta", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            return true;
        }
    }

    private void insertarDepartamento() {
//        System.out.println("0000000000 Ingresando Departamentos 00000000000");
        cd.setDepartamento(null);
        String[] s = new String[4];
        s[0] = txtContra.getText();
        s[1] = txtCapacidad.getText();
        s[2] = txtNombre.getText();
        s[3] = jComboBox1.getSelectedItem().toString();
        if (cd.ingresarDepartamento(s)) {
            limpiar();
            System.out.println("Se ingreso el departamento correctamente");
            JOptionPane.showMessageDialog(null, "Se ingreso el departamento correctamente");
        } else {
            System.err.println("No se ingreso el departamento");
            JOptionPane.showMessageDialog(null, "No se ingreso el departamento");
        }
        cd.setDepartamento(null);
//        cd.mostrarDepartamentos();

//        System.out.println("000000000000000000000000000000000000000000000000");
    }
    
