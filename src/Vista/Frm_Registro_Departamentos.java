    private Boolean capacidadEstaCorecta() {
        Pattern p = Pattern.compile("(^[0-9]{1,1}$)");
        Matcher m = p.matcher(txtCapacidad.getText());
        if (m.matches() && (Integer.valueOf(txtCapacidad.getText()) >= 1 && Integer.valueOf(txtCapacidad.getText()) <= 7)) {
            return true;
        } else {
            return false;
        }
    }
    
    private Boolean contrasenaEstaCorrecta() {
        Pattern p = Pattern.compile("(^.+$)");
        Matcher m = p.matcher(txtContra.getText());
        if (m.matches() && (txtContra.getText().length() > 5 && txtContra.getText().length() < 10)) {
            return true;
        } else {
            return false;
        }
        
    }
    
